package Linked_list.Singly_list_list;

public class Defolt {
    int a;
    int b;
    int c;

    Defolt() {

        a = 10;
        b = 20;
        c = a + b;
    }

    void show() {
        System.out.println(a + " " + b + " " + c);
    }

    public static void main(String[] args) {

        Defolt f = new Defolt();
        f.show();

    }

}
