package Array;

public class Age {
    public static void main(String[] args) {
        int age[] = new int[5];

        age[0] = 20;
        age[1] = 30;
        age[2] = 40;
        age[3] = 50;
        age[4] = 60;
        for (int i = 0; i < age.length; i++) {
            System.out.println(age[i]);
        }

    }

}
